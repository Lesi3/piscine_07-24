#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	condition(char a, char b, char c)
{
	if (c > b && b > a)
	{
		ft_putchar(a);
		ft_putchar(b);
		ft_putchar(c);
		if (a != '7' || b != '8' || c != '9')
		{
			ft_putchar(',');
			ft_putchar(' ');
		}
	}
}

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;

	a = '0';
	while (a <= '7')
	{
		b = '1';
		while (b <= '8')
		{
			c = '2';
			while (c <= '9')
			{
				condition (a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}

